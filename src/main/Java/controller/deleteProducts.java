    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        // Get the product ID to delete from the request
        String productId = request.getParameter("productId");
        if (productId == null || productId.trim().isEmpty()) {
            // Handle the case where product ID is not provided
            response.sendError(HttpServletResponse.SC_BAD_REQUEST, "Product ID is required");
            return;
        }

        ProductsDAO p = new ProductsDAO(); 
        boolean isDeleted = p.deleteProduct(productId); 

        if (isDeleted) {
            // If deletion was successful, redirect to the list of products
            response.sendRedirect("showProducts.jsp");
        } else {
            // If deletion failed, you might want to send back an error or redirect to an error page
            response.sendError(HttpServletResponse.SC_INTERNAL_SERVER_ERROR, "Unable to delete the product");
        }
    }

